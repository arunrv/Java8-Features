package programming;

public class SwitchExample 
{
	static int n=5;
	
	public static void main(String[] args) {
		
		
		switch (1) {
		case 1:
			System.out.println("Sunday");
			break;
			
		case 2:
			System.out.println("Monday");
			break;
			
		case 3:
			System.out.println("Tuesday");
			break;
			
		case 5:
			System.out.println("Wednessday");
			break;

		default:
			System.out.println("No day");
			break;
		}
	}

}
